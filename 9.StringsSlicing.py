Fruit = "Mango"
#length of string
print("length of Fruit--->",len(Fruit))

#slicing of string
print("positive slicing---->",Fruit[1:4]) # positive slicing
print("negative slicing---->",Fruit[-3:]) # negative slicing
print("slicing from start--->",Fruit[:5]) #Slicing from start
print("slicing from end--->",Fruit[4:]) #Slicing from end