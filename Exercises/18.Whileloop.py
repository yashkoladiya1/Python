
print("incremental---")
#incremental while Loop
i=0
while(i<=3):
    print(i)
    i=i+1

print("decremental---")
#decremental while loop
i=5
while(i>=0):
    print(i)
    i=i-1

print("with else---")
#decremental while loop with else
i=5
while(i>=0):
    print(i)
    i=i-1
else:
    print("I am inside the else")